# Tabs or Spaces?

# Spaces are the preferred indentation method.
# Tabs should be used solely to remain consistent with code
# that is already indented with tabs.
# Python 3 disallows mixing the use of tabs and spaces for
# indentation.
# Python 2 code indented with a mixture of tabs and spaces
# should be converted to using spaces exclusively.
# When invoking the Python2 command line interpreter with
# the -t option, it issued warnings about code that illegaly
# mixes tabs and spaces. Then using -tt theses warnings become
# errors. The options are highly recommended.

